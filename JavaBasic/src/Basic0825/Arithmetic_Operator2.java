package Basic0825;

//ex2 산술연산자)
public class Arithmetic_Operator2 {

	public static void main(String[] args) {
		System.out.println(20+3);
		System.out.println(20-3);
		System.out.println(20*3);
		System.out.println(20/3); // 몫
		System.out.println(20%3); // 나머지
		System.out.println(3+2*4);
		System.out.println((3+2)*4);
	}

}
